public class 연습문제7 {
    public static void main(String[] args) {
        int a = 0;
        int b = 1;
        int c = 2;

        if(a!=0 && b!=0) System.out.print(1+" ");
        else System.out.print(0+" ");

        if(a!=0 || b!=0) System.out.print(1+" ");
        else System.out.print(0+" ");

        if(c!=0 && b!=0) System.out.print(1+" ");
        else System.out.print(0+" ");

        if(a == 0) System.out.print(1+" ");
        else System.out.print(0+" ");
    }
}
