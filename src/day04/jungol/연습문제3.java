package day04.jungol;

public class 연습문제3 {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;

        System.out.printf("최초값 a = %d, b = %d\n", a,b);
        System.out.printf("a++ = %d, ++b = %d\n", a++, ++b);
        System.out.printf("실행 후 a = %d, b = %d\n", a,b);

        System.out.printf("a-- = %d, --b = %d\n", a--, --b);
        System.out.printf("실행 후 a = %d, b = %d\n", a,b);
    }
}
